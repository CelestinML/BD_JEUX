CREATE OR REPLACE TRIGGER Suivi_Reseau_TRG
    AFTER INSERT OR UPDATE
    ON RESEAU
    FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO SUIVI_RESEAU(CODERESEAU, DATESUIVI, ACTION)
        VALUES(:NEW.CODE, SYSDATE(), 'CRE');
    ELSIF UPDATING THEN
        IF (:NEW.ACTIF = '1') THEN
            INSERT INTO SUIVI_RESEAU(CODERESEAU, DATESUIVI, ACTION)
            VALUES(:NEW.CODE, SYSDATE(), 'ACT');
        ELSE
            INSERT INTO SUIVI_RESEAU(CODERESEAU, DATESUIVI, ACTION)
            VALUES(:NEW.CODE, SYSDATE(), 'DES');
        END IF;
    END IF;
END;